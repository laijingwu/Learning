#include <stdio.h>

int main()
{
	int x;
	double y;
	while (scanf("%d", &x) != EOF)
	{
		switch (x / 100000)
		{
		case 0:
			y = x * 0.1;
			break;
		case 1:
			if (x == 100000)
				y = x * 0.1;
			y = (x - 100000) * 0.075 + 100000 * 0.1;
			break;
		case 2:
		case 3:
			if (x == 200000)
				y = (x - 100000) * 0.075 + 100000 * 0.1;
			y = (x - 200000) * 0.05 + 100000 * (0.075 + 0.1);
			break;
		case 4:
		case 5:
			if (x == 400000)
				y = (x - 200000) * 0.05 + 100000 * (0.075 + 0.1);
			y = (x - 400000) * 0.03 + 200000 * 0.05 + 100000 * (0.075 + 0.1);
			break;
		case 6:
		case 7:
		case 8:
		case 9:
			if (x == 600000)
				y = (x - 400000) * 0.03 + 200000 * 0.05 + 100000 * (0.075 + 0.1);
			y = (x - 600000) * 0.015 + 200000 * 0.03 + 200000 * 0.05 + 100000 * (0.075 + 0.1);
			break;
		default:
			if (x == 1000000)
				y = (x - 600000) * 0.015 + 200000 * 0.03 + 200000 * 0.05 + 100000 * (0.075 + 0.1);
			else if (x > 1000000)
				y = (x - 1000000) * 0.01 + 400000 * 0.015 + 200000 * 0.03 + 200000 * 0.05 + 100000 * (0.075 + 0.1);
			break;
		}
		printf("%.0f\n", y);
	}
	return 0;
}
